---@module 'utils.assert'
---
---Provide flexible assertion utilities with configurable error handling and grouping.
---
---Support multiple assertion strategies including immediate failure, error accumulation,
---custom handlers, and method chaining. Organize assertions into logical groups (string,
---number, table, type, nil, comparison) for clarity and maintainability.
---
---### Example Usage
---~~~lua
---local Assert = require "utils.assert"
---
----- Immediate failure mode
---local assert = Assert.new()
---assert.str:not_empty("test")
---assert.num:in_range(5, 0, 10)
---
----- Accumulate errors for batch validation
---local validator = Assert.new({ accumulate = true, prefix = "[Config] " })
---validator.tbl:has_key(config, "host")
---validator.num:in_range(config.port, 1, 65535)
---if validator:count() > 0 then
---  validator:throw()
---end
---
----- Chained assertions with value holder
---assert:that(email).str
---  :not_empty()
---  :matches("@")
---  :min_length(5)
---~~~
